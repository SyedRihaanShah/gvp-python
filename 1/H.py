# Logical Operators
a = 10
b = 5

print("Logical Operators")
print("a > b and b > 0 :", a > b and b > 0)
print("a > b or b < 0  :", a > b or b < 0)
print("not(a > b)     :", not(a > b))

print("\nBitwise Operators")

# Bitwise Operators
x = 6   
y = 3

print("x & y  :", x & y)    # AND
print("x | y  :", x | y)    # OR
print("x ^ y  :", x ^ y)    # XOR
print("~x     :", ~x)       # NOT
print("x << 1 :", x << 1)   # Left shift
print("x >> 1 :", x >> 1)   # Right shift
